
// Date in label display
var dateInBtn = document.querySelector("#DateInImg");
var dateInlabel = document.querySelector("#lblDateIn");

dateInBtn.addEventListener('mouseover', () => {
    dateInlabel.style.visibility = "visible";
});

dateInBtn.addEventListener('mouseout', () => {
    dateInlabel.style.visibility = "hidden";
});


